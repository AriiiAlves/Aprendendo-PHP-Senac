<?php

# Adição -> +
# Subtração -> -
# Multiplicação -> *
# Divisão -> /
# Exponenciação -> *
# Resto da divisão -> %

$n1 = 30;
$n2 = 7;

echo("n1: " . $n1 . "<br>" . "n2:" . $n2 . "<br><br>");
echo("Soma: $n1 + $n2 = " . $n1 + $n2 . "<br>");
echo("Subtração: $n1 - $n2 = " . $n1 - $n2 . "<br>");
echo("Multiplicação: $n1 * $n2 = " . $n1 * $n2 . "<br>");
echo("Divisão: $n1 / $n2 = " . $n1 / $n2 . "<br>");
echo("Exponenciação: $n1 ^ $n2 = " . $n1 ** $n2 . "<br>");
echo("Resto da divisão: $n1 % $n2 = " . $n1 % $n2 . "<br>");

?>